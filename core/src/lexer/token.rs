use foundation::span::Span;

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum TokenKind {
    Identifier,
    Integer,
    Float,
    String,
    Char,
    Bool,
    TypeName,
    Plus,
    Minus,
    Star,
    DoubleStar,
    Slash,
    Percent,
    Bang,
    Tilde,
    Ampersand,
    Pipe,
    Caret,
    ShiftLeft,
    ShiftRight,
    AndAnd,
    OrOr,
    EqualEqual,
    BangEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Comma,
    Semicolon,
    Colon,
    DoubleColon,
    Assign,
    InferAssign,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Token {
    pub kind: TokenKind,
    pub lexeme: String,
    pub span: Span,
}
